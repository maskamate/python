# Mire Jók a halmazok?
# A halmaz (set) egy beépített Python adattípus, 
# aminek segítségével több elemet tárolhatunk egyetlen változóban. 
# A halmazoknak több egyedi tulajdonsága is van: rendezetlenek és nem rendelkeznek indexekkel. 
# Továbbá a halmazok nem tartalmazhatnak duplikált értékeket, de bármilyen típusú objektumot befogadnak, 
# ha azok megváltoztathatatlanok. Tehát a halmazok csak stringeket, 
# számokat vagy tuple-öket tartalmazhatnak, dictionaryket és más listákat nem.

